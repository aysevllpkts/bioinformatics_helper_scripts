#!/bin/bash



########################################################################################################################################################################################################################


ASSEMBLY=$1
OUT_DIR=$2
SR_IN=$3
NAME=$4

# remapping ILLUMINA reads to transcriptome 
bowtie2 -p 8 -q --no-unal -k 20 -x $ASSEMBLY \
 -1 ${SR_IN}/${NAME}_1.trimmed.fq.gz -2 ${SR_IN}/${NAME}_2.trimmed.fq.gz 2>${OUT_DIR}/${NAME}_align.stats.txt | samtools view -Sb -@ 1 -m 1G  | samtools sort -@ 1 -m 1G -o ${OUT_DIR}/${NAME}.bowtie2.sorted.bam

########################################################################################################################################################################################################################




